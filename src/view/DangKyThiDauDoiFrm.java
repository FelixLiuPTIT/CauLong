    private void btnDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangKyActionPerformed
        // TODO add your handling code here:
        Doi d1 = null;
        Doi d2 = null;
        if(cbDoi1.getSelectedIndex() >= 0 && cbDoi2.getSelectedIndex() >= 0) {
            d1 = listDoi[cbDoi1.getSelectedIndex()];
            d2 = listDoi[cbDoi2.getSelectedIndex()];
        }
        if(d1.getId() == d2.getId()) {
            JOptionPane.showMessageDialog(null, "Khong duoc de trung doi");
            return;
        }
        DangKyThiDauDoi dk1 = new DangKyThiDauDoi(d1, true, "Doi dang ky dau tien");
        DangKyThiDauDoi dk2 = new DangKyThiDauDoi(d2, false, "Doi dang ky thu 2");
        ArrayList<DangKyThiDauDoi> listDK = new ArrayList<DangKyThiDauDoi>();
        listDK.add(dk1);
        listDK.add(dk2);
        trandau.setListDangKyDoi(listDK);
        if(clientCtr.dangKyThiDauDoi(trandau)) {
            JOptionPane.showMessageDialog(null, "Dang ky thanh cong");
            dispose();
        }
        else {
            JOptionPane.showMessageDialog(null, "Dang ky khong thanh cong");
        }
    }//GEN-LAST:event_btnDangKyActionPerformed

