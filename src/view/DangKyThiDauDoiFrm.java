    private void cbDoi1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbDoi1ItemStateChanged
        // TODO add your handling code here:
        if(listDoi != null && cbDoi1.getSelectedIndex() >= 0) {
            Doi d = listDoi[cbDoi1.getSelectedIndex()];
            if(d.getListVDV() == null) {
                txtTen11.setText("");
                txtTen12.setText("");
                return;
            }
            txtTen11.setText(d.getListVDV().get(0).getHoten());
            txtTen12.setText(d.getListVDV().get(1).getHoten());
        }
    }//GEN-LAST:event_cbDoi1ItemStateChanged

