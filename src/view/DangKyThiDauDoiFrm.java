    private void initThongTinLich() {
        txtNoiDung.setText(trandau.getNoiDung().getTen());
        txtVong.setText(""+trandau.getVong());
        txtCap.setText(""+trandau.getCap());
        txtThoiGian.setText(trandau.getThoiGian().toString());
        txtSan.setText(trandau.getSan().getTen());
    }
    
    private void initListDoi() {
        cbDoi1.removeAllItems();
        cbDoi2.removeAllItems();
        NoiDung nd = trandau.getNoiDung();
        listDoi = clientCtr.getDoiByNoiDung(nd);
        if(listDoi != null) {
            for(Doi d: listDoi) {
                cbDoi1.addItem(d.getTenDoi());
                cbDoi2.addItem(d.getTenDoi());
            }
        }
    }
    
    private void initListVDV1() {
        if(listDoi != null) {
            Doi d = listDoi[cbDoi1.getSelectedIndex()];
            txtTen11.setText(d.getListVDV().get(0).getHoten());
            txtTen12.setText(d.getListVDV().get(1).getHoten());
        }
    }
    
    private void initListVDV2() {
        if(listDoi != null) {
            Doi d = listDoi[cbDoi2.getSelectedIndex()];
            txtTen21.setText(d.getListVDV().get(0).getHoten());
            txtTen22.setText(d.getListVDV().get(1).getHoten());
        }
    }

